package BasicOfJava;

public class DeclareMultipleVariables {

    public static void main(String[] args) {

        //simple variable assign eg.1
        int x = 10;
        int y = 20;
        int z = 30;

        System.out.println("Addition is: "+(x+y+z));


        //simple variable assign eg.2
        int a = 5, b = 10, c = 15;
        System.out.println("Addition is: "+(a+b+c));

        //One Value to Multiple Variables
        int l, m, n;
        l = m = n = 10; // l = 10, m = 10, n = 10



    }
}
